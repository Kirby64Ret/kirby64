f32 asinf(f32 x) {
    if (0.9999899864f < x) {
        return M_PIF/2;
    }
    if (x < -0.9999899864f) {
        return -M_PIF/2;
    }
    return atanf(x / sqrtf(1.0f - x * x));
}

f32 acosf(f32 x) {
    if (0.9999899864f < x) {
        return 0.0f;
    }
    if (x < -0.9999899864f) {
        return M_PIF;
    }
    return (M_PIF/2) - atanf(x / sqrtf(1.0f - x * x));
}

f32 cscf(f32 x) {
    return 1.0f / sinf(x);
}

f32 secf(f32 x) {
    return 1.0f / cosf(x);
}

f32 cotf(f32 x) {
    return 1.0f / tanf(x);
}

